public class GearSet {
    String gearSetName;
    String icon;
    String setBonus2;
    String setBonus3;
    String setBonus4;
    String bodyArmorTalent;
    String backpackTalent;

    public GearSet(String gearSetName, String icon, String setBonus2, String setBonus3, String setBonus4, String bodyArmorTalent, String backpackTalent) {
        this.gearSetName = gearSetName;
        this.icon = icon;
        this.setBonus2 = setBonus2;
        this.setBonus3 = setBonus3;
        this.setBonus4 = setBonus4;
        this.bodyArmorTalent = bodyArmorTalent;
        this.backpackTalent = backpackTalent;
    }

}
