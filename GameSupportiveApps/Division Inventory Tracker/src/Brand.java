public class Brand {
    String brandSetName;
    String icon;
    String coreAttribute;
    String setBonus1;
    String setBonus2;
    String setBonus3;

    public Brand(String brandSetName, String icon, String coreAttribute, String setBonus1, String setBonus2, String setBonus3) {
        this.brandSetName = brandSetName;
        this.icon = icon;
        this.coreAttribute = coreAttribute;
        this.setBonus1 = setBonus1;
        this.setBonus2 = setBonus2;
        this.setBonus3 = setBonus3;
    }
}
