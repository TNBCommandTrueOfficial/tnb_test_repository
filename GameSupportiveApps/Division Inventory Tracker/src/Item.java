public class Item {
    String itemFamily;
    String itemType;
    String itemName;
    String itemBrand;
    String isGearSetPiece;

    public Item(String itemFamily, String itemType, String itemName, String itemBrand, String isGearSetPiece) {
        this.itemFamily = itemFamily;
        this.itemType = itemType;
        this.itemName = itemName;
        this.itemBrand = itemBrand;
        this.isGearSetPiece = isGearSetPiece;
    }
}
